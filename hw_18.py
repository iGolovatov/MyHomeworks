# Пустой список для хранения результатов
result_list = []

# Запрос кол-ва пословиц
proverbs_count = input('Введите кол-во пословиц: ')
while not proverbs_count.isdigit():
    proverbs_count = input('Вы ввели не число! Повторите ввод! Кол-во пословиц: ')

