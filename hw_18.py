# Убираем дубликаты путем перевода массива в множество
proverbs = list(set(proverbs))
variants = list(set(variants))

# Цикл для генерации пословиц
while len(result_list) < proverbs_count and proverbs and variants:
    random.shuffle(proverbs)
    random_proverb = proverbs.pop()

    random.shuffle(variants)
    random_variant = variants.pop()

    new_proverb = random_proverb.lower().replace('ум', random_variant).capitalize()
    result_list.append(new_proverb)

# Вывод пословиц в консоль
[print(proverb) for proverb in result_list]
